import React from 'react'
import PropTypes from 'prop-types'
import classNames from 'classnames'

import PostQuoteBlock from './PostQuoteBlock'
import { postInlineQuote } from '../PropTypes'

import './PostInlineQuote.css'

export default function PostInlineQuote({ autogenerated, kind, children }) {
	return (
		<span className={classNames('post__inline-quote', 'post__quote-blocks')}>
			<PostQuoteBlock inline autogenerated={autogenerated} kind={kind}>
				{children}
			</PostQuoteBlock>
		</span>
	)
}

PostInlineQuote.propTypes = {
	autogenerated: PropTypes.bool,
	// `kohlchan.net` and `8ch.net` have regular quotes and "inverse" quotes.
	kind: PropTypes.string,
	children: PropTypes.node.isRequired
}

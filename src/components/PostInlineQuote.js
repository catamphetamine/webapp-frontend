import React from 'react'
import PropTypes from 'prop-types'
import { Link } from 'react-website'
import classNames from 'classnames'

import { postInlineQuote } from '../PropTypes'

import './PostInlineQuote.css'

export default function PostInlineQuote({ url, autogenerated, kind, children }) {
	const className = classNames('post__inline-quote', 'post__quote-block', {
		'post__inline-quote--link': url
	})
	const quote = (
		<QuoteBlock autogenerated={autogenerated} kind={kind}>
			{children}
		</QuoteBlock>
	)
	if (url) {
		if (url[0] === '/') {
			return (
				<Link
					to={url}
					className={className}>
					{quote}
				</Link>
			)
		}
		return (
			<a
				target={url[0] === '#' ? undefined : '_blank'}
				href={url}
				className={className}>
				{quote}
			</a>
		)
	}
	return (
		<span className={className}>
			{quote}
		</span>
	)
}

PostInlineQuote.propTypes = {
	url: PropTypes.string,
	autogenerated: PropTypes.bool,
	// `kohlchan.net` and `8ch.net` have regular quotes and "inverse" quotes.
	kind: PropTypes.string,
	children: PropTypes.node.isRequired
}

function QuoteBlock({ kind, autogenerated, children }) {
	// `<span/>`s are used instead of `<div/>`s
	// because the parent tag is `<a/>`
	// which can't contain block-level DOM elements.
	return (
		<span className={classNames(
			'post__quote-block__content',
			'post__inline-quote__block',
			kind && `post__inline-quote__block--${kind}`
		)}>
			<span className="post__quote-block__marker"/>
			{/* Set the content to "> " for copy-pasting quotes. */}
			{/* It won't be visible due to `font-size: 0`. */}
			<span className="post__inline-quote__prefix">
				{'> '}
			</span>
			<q
				className={classNames('post__inline-quote__content', {
					'post__inline-quote__content--autogenerated': autogenerated
				})}>
				{children}
			</q>
		</span>
	)
}

QuoteBlock.propTypes = {
	// `kohlchan.net` and `8ch.net` have regular quotes and "inverse" quotes.
	kind: PropTypes.string,
	autogenerated: PropTypes.bool,
	content: PropTypes.node.isRequired
}